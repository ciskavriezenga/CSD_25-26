//==============================================================================
void AudioPluginAudioProcessor::getStateInformation (juce::MemoryBlock& destData) {
    auto state = Params.copyState();
    std::unique_ptr<juce::XmlElement> xml (state.createXml());
    copyXmlToBinary (*xml, destData);
}

void AudioPluginAudioProcessor::setStateInformation (const void* data, int sizeInBytes) {
    std::unique_ptr<juce::XmlElement> xmlState (getXmlFromBinary (data, sizeInBytes));
    if (xmlState.get() != nullptr)
        if (xmlState->hasTagName (Params.state.getType()))
            Params.replaceState (juce::ValueTree::fromXml (*xmlState));
}

juce::AudioProcessorValueTreeState::ParameterLayout AudioPluginAudioProcessor::createParameterLayout(AudioPluginAudioProcessor::Parameters& parameters){
    using namespace juce;
    AudioProcessorValueTreeState::ParameterLayout layout;
    {
        auto parameter = std::make_unique<AudioParameterFloat>(id::PARAM, "PARAM", 0.0, 1.0, 0.0);
        parameters.Param = parameter.get();
        layout.add(std::move(parameter));
    }
    return layout;
}


